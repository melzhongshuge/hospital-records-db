CREATE DATABASE IF NOT EXISTS hospital_db;
USE hospital_db;

CREATE TABLE PATIENT (
    PATIENT_ID INT PRIMARY KEY,
    FIRST_NAME VARCHAR(255),
    LAST_NAME VARCHAR(255),
    DOB DATE,
    GENDER VARCHAR(255),
    CONTACT_NUMBER VARCHAR(255)
);

