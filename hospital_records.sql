CREATE TABLE MEDICAL_RECORD (
    RECORD_ID INT PRIMARY KEY,
    PATIENT_ID INT,
    DIAGNOSIS VARCHAR(255),
    TREATMENT VARCHAR(255),
    ADMISSION_DATE DATE,
    DISCHARGE_DATE DATE,
    FOREIGN KEY (PATIENT_ID) REFERENCES PATIENT(PATIENT_ID)
    );
    
